// Exercicio 2 - Calcule o tempo de uma viagem de carro. Pergunte a distância a percorrer e a velocidade média esperada para a viagem.

let distanciaPercorrida = 900
let VelocidadeMedia = 80
let tempoViagem = (distanciaPercorrida / VelocidadeMedia).toFixed(0)

console.log ("Para uma viagem de " + (distanciaPercorrida) + "km de distância em uma velocidade média de " + (VelocidadeMedia) + "km/h, a duração será de aproximadamente " + (tempoViagem) + " horas")

// Resultado esperado: Para uma viagem de 900km de distância em uma velocidade média de 80km/h, a duração será de aproximadamente 11 horas
